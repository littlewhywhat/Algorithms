package com.littlewhywhat.algorithms.twosum;

public class TwoSumConfig {
	private int lowerBound;
	private int upperBound;
	int getLowerBound() {
		return lowerBound;
	}
	int getUpperBound() {
		return upperBound;
	}
	public TwoSumConfig(int lowerBound, int upperBound) {
		this.lowerBound = lowerBound;
		this.upperBound = upperBound;
	}
	
}
